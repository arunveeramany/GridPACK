#include <stdbool.h>
#include <gridpack/math/math.hpp>

// -------------------------------------------------------------
// gridpack_initialize_math
// -------------------------------------------------------------
extern "C" void 
gridpack_initialize_math(void)
{
  gridpack::math::Initialize();
}

// -------------------------------------------------------------
// gridpack_math_initialized
// -------------------------------------------------------------
extern "C" _Bool
gridpack_math_initialized(void)
{
  return static_cast<_Bool>(gridpack::math::Initialized());
}

// -------------------------------------------------------------
// gridpack_finalize_math
// -------------------------------------------------------------
extern "C" void 
gridpack_finalize_math(void)
{
  gridpack::math::Finalize();
}

